public class NumbePositiveOrNegative {
    public static void main(String[] args) {
        int n = -1;
        if(n>0) {
            System.out.println(n + " : Positive Number ");
        }
        else if(n<0){
            System.out.println(n+" : negative number ");
        }
        else { 
            System.out.println(n+" : zero");
        }
    }
}
